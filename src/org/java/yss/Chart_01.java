package org.java.yss;

/**
 * 字符型
 * @author Administrator
 *
 */
public class Chart_01 {
	public static void main(String[] args) {
		char a = '陈';
		char b = '辉';
		int c = 38472;
		int d = 36745;
		System.out.println("陈 在Unicode码中对应："+(int)a);
		System.out.println("辉 在Unicode码中对应："+(int)b);
		System.out.println("38472在Unicode码中对应："+(char)c);
		System.out.println("36745在Unicode码中对应："+(char)d);
	}
}
